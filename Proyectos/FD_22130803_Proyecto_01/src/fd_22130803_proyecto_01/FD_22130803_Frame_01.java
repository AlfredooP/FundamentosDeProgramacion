    //Función que calcula la Constante Elástica (k)
    public static double ConstanteK(double EM, double m, double v, double h, double x)
    {
        double k, g;
        g = 9.8;
        k = (EM - (m*Math.pow(v,2)/2) - m*g*h) * 2 / Math.pow(x,2);
        return k;
    }
    
    //Función que calcula la Energía Cinética (EC)
    public static double EnergiaC (double m, double v)
    {
        double EC;
        EC = (m*Math.pow(v,2))/2;
        return EC;
    }
    
    //Función que calcula la Energía Potencial Gravitacional (EPG)
    public static double EnergiaPG(double m, double h)
    {
        double EPG, g;
        g = 9.8;
        EPG = (m*g*h);
        return EPG;
    }
    
    //Función que calcula la Energía Potencial Elástica (EPE)
    public static double EnergiaPE( double EM, double EC, double EPG)
    {
        double EPE;
        EPE = (EM - EC - EPG);
        return EPE;
    }

