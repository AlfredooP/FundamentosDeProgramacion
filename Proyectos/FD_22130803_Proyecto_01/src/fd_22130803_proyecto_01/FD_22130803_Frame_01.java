    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        //Declaración de variables
        double m1, v1, h1, x1, k1, EC1, EPG1, EPE1, EM1,
               m2, v2, h2, x2, k2, EC2, EPG2, EPE2, EM2,
               m3, v3, h3, x3, k3, EC3, EPG3, EPE3, EM3,
               g, Promedio;
        
        //Entrada de datos
        m1 = Double.parseDouble(jTextM1.getText());
        m2 = Double.parseDouble(jTextM2.getText());
        m3 = Double.parseDouble(jTextM3.getText());
        
        v1 = Double.parseDouble(jTextV1.getText());
        v2 = Double.parseDouble(jTextV2.getText());
        v3 = Double.parseDouble(jTextV3.getText());
        
        h1 = Double.parseDouble(jTextH1.getText());
        h2 = Double.parseDouble(jTextH2.getText());
        h3 = Double.parseDouble(jTextH3.getText());
        
        x1 = Double.parseDouble(jTextX1.getText());
        x2 = Double.parseDouble(jTextX2.getText());
        x3 = Double.parseDouble(jTextX3.getText());
        
        EM1 = Double.parseDouble(jTextEM1.getText());
        EM2 = Double.parseDouble(jTextEM2.getText());
        EM3 = Double.parseDouble(jTextEM3.getText());
        
        //Calculamos la constante elástica (k) con la función ConstanteK
        k1 = ConstanteK(EM1, m1, v1, h1, x1);
        k2 = ConstanteK(EM2, m2, v2, h2, x2);
        k3 = ConstanteK(EM3, m3, v3, h3, x3);
        
        //Calculamos la Energía Cinética (EC) con la función EnergiaC
        EC1 = EnergiaC(m1, v1);
        EC2 = EnergiaC(m2, v2);
        EC3 = EnergiaC(m3, v3);
        
        //Calculamos la Energía Potencial Gravitacional (EPG) con la función EnergiaPG
        EPG1 = EnergiaPG(m1, h1);
        EPG2 = EnergiaPG(m2, h2);
        EPG3 = EnergiaPG(m3, h3);
        
        //Calculamos la Energía Potencial Elástica (EPE) con la función EnergiaPE
        EPE1 = EnergiaPE(EM1, EC1, EPG1);
        EPE2 = EnergiaPE(EM2, EC2, EPG2);
        EPE3 = EnergiaPE(EM3, EC3, EPG3);
        
        //Calculamos el promedio de nuestro dato (k)
        Promedio = (k1 + k2 + k3) / 3;
        
        //Salida de datos
        jLabelK1.setText(k1 + "");
        jLabelK2.setText(k2 + "");
        jLabelK3.setText(k3 + "");
        
        jLabelEC1.setText(EC1 + "");
        jLabelEC2.setText(EC2 + "");
        jLabelEC3.setText(EC3 + "");
        
        jLabelEPG1.setText(EPG1 + "");
        jLabelEPG2.setText(EPG2 + "");
        jLabelEPG3.setText(EPG3 + "");
        
        jLabelEPE1.setText(EPE1 + "");
        jLabelEPE2.setText(EPE2 + "");
        jLabelEPE3.setText(EPE3 + "");
        
        jLabelPromedio.setText(Promedio + "");
        
    }//GEN-LAST:event_jButtonCalcularActionPerformed

