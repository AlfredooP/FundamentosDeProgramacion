    public int LetraANumero(char letra)
    {
        int num;
        
        switch(letra)
        {
            case 'a':
            case 'A': num = 100; break;
            
            case 'b':
            case 'B': num = 90; break;
            
            case 'c':
            case 'C': num = 80; break;
            
            case 'd':
            case 'D': num = 70; break;
            
            case 'e':
            case 'E': num = 60; break;
            
            default: num = 0;
            
        }
        
        return num;
    }
    
    public char CalificacionLetra()
    {
        Random azar = new Random();
        
        char letra = 'E';
        
        switch(azar.nextInt(5))
        {
            case 0: letra = 'A'; break;
            case 1: letra = 'B'; break;
            case 2: letra = 'C'; break;
            case 3: letra = 'D'; break;
            case 4: letra = 'E'; break;
        }
        return letra;
    }
    
