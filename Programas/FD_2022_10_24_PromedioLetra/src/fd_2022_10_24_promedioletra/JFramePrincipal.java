    private void jButtonPromediarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPromediarActionPerformed
        // TODO add your handling code here:
        int examen1N, examen2N, examen3N, examen4N;
        char examen2L, examen3L, examen4L;
        double promedio;
        String nivel;
        
        examen1N = Integer.parseInt(jTextExamen1.getText());
        examen2L = jTextExamen2.getText().charAt(0);
        examen3L = jTextExamen3.getText().charAt(0);
        examen4L = jTextExamen4.getText().charAt(0);
        
        examen2N = LetraANumero(examen2L);
        examen3N = LetraANumero(examen3L);
        examen4N = LetraANumero(examen4L);
        
        promedio = (examen1N + examen2N + examen3N+ examen4N)/4.0;
        
        if(promedio < 60)
            nivel = "Insuficiente";
        else
            if(promedio < 70)
                nivel = "Suficiente";
            else
                if(promedio < 80)
                    nivel = "Regular";
                else
                    if(promedio < 90)
                        nivel = "Bueno";
                    else
                        nivel = "EXCELENTE";
        
        jLabelPromedio.setText(promedio + "");
        jLabelNivel.setText(nivel + "");
    }//GEN-LAST:event_jButtonPromediarActionPerformed

